    private void tampilkan_data(){
        
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No.");
        model.addColumn("NOMOR REGISTRASI");
        model.addColumn("MERK");
        model.addColumn("TYPE");
        model.addColumn("TAHUN");
        model.addColumn("WARNA");
        model.addColumn("GRADE KENDARAAN");
        
        try{
            int no = 1;
            String sql = "select* from listkendaraan";
            java.sql.Connection conn=(Connection)koneksi.configDB();
            java.sql.Statement st = conn.createStatement();
            java.sql.ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                model.addRow (new Object[]{no++,rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)});
            }
            tabellistkendaraan.setModel(model);
        }catch(SQLException e){
            System.out.println("Error :"+e.getMessage());
        }
    }
    
