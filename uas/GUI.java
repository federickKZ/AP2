    private void tabellistkendaraanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabellistkendaraanMouseClicked
        // TODO add your handling code here:
        int baris = tabellistkendaraan.rowAtPoint(evt.getPoint());
        String noreg = tabellistkendaraan.getValueAt(baris, 1).toString();
        txtNOREG.setText(noreg);
        
        String merk= tabellistkendaraan.getValueAt(baris, 2).toString();
        txtMERK.setText(merk);
        
        String type= tabellistkendaraan.getValueAt(baris, 3).toString();
        txtTYPE.setText(type);
        
        String tahun= tabellistkendaraan.getValueAt(baris, 4).toString();
        txtTAHUN.setText(tahun);
        
        String warna= tabellistkendaraan.getValueAt(baris,5).toString();
        txtWARNA.setText(warna);
        
        String gradekendaraan = tabellistkendaraan.getValueAt(baris, 6).toString();
        cbGRADE.setSelectedItem(gradekendaraan);
    }//GEN-LAST:event_tabellistkendaraanMouseClicked

